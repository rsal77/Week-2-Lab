# Variable for speed of car
speed = 70

# Calculate the distance of the distance traveled over time
sixHours = speed * 6
tenHours = speed * 10
fifteenHours = speed * 15

# Display the distance travled over time
print(f'The car will travel {sixHours:.1f} miles in 6 hours.')
print(f'The car will travel {tenHours:.1f} miles in 10 hours.')
print(f'The car will travel {fifteenHours:.1f} miles in 15 hours.')